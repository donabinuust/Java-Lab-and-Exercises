
public class p5_relational_operators {

	public static void main(String[] args) {
		System.out.println("5==5 : " + (5==5));
		System.out.println("5!=7 : " + (5!=7));
		System.out.println("5>7 : " + (5>7));
		System.out.println("5>=7 : " + (5>=7));
		System.out.println("5<7 : " + (5<7));
		System.out.println("5<=7 : " + (5<=7));
	}

}
